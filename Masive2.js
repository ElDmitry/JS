const findValueByKey = (companyObj, companyName) => {
    const values = [...companyObj.clients];

    while (values.length !== 0) {
        const current = values.shift();
        if (current.name === companyName) {
            return current;
        }
        if (current.partners && current.partners.length !== 0) {
            values.push(...current.partners);
        }
    }

    return null;
}
